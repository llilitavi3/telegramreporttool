@Composable
fun LandingScreen(
    onGetStarted: () -> Unit
) {
    Surface(
        modifier = Modifier.fillMaxSize(),
        color = MaterialTheme.colorScheme.background
    ) {
        Column(
            modifier = Modifier
                .fillMaxSize()
                .padding(24.dp)
                .verticalScroll(rememberScrollState()),
            horizontalAlignment = Alignment.CenterHorizontally,
            verticalArrangement = Arrangement.Center
        ) {
            Icon(
                imageVector = Icons.Default.Shield,
                contentDescription = null,
                modifier = Modifier.size(100.dp),
                tint = MaterialTheme.colorScheme.primary
            )
            
            Spacer(modifier = Modifier.height(32.dp))
            
            Text(
                text = "Telegram Report Tool",
                style = MaterialTheme.typography.headlineLarge,
                fontWeight = FontWeight.Bold,
                textAlign = TextAlign.Center
            )
            
            Text(
                text = "Community Safety & Accountability",
                style = MaterialTheme.typography.titleMedium,
                color = MaterialTheme.colorScheme.secondary,
                textAlign = TextAlign.Center
            )
            
            Spacer(modifier = Modifier.height(48.dp))
            
            FeatureRow(
                icon = Icons.Default.Warning,
                title = "Report Violations",
                description = "Quickly report spam, scams, and harmful content to Telegram."
            )
            
            Spacer(modifier = Modifier.height(24.dp))
            
            FeatureRow(
                icon = Icons.Default.Group,
                title = "Multi-Account Support",
                description = "Manage and report from multiple Telegram accounts seamlessly."
            )
            
            Spacer(modifier = Modifier.height(64.dp))
            
